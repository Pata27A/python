#concatenacion o suma de valores

numero1_cadena = '10'
print(f'Numero 1 en cadena: {numero1_cadena}')

numero2_cadena = '20'
print(f'Numero 2 en cadena: {numero2_cadena}')
#concatenacion
resultado = numero1_cadena + numero2_cadena
print(f'Resultado: {resultado}')

#suma de valores
numero1_cadena = '10'
numero1 = int(numero1_cadena)
print(f'Numero 1: {numero1}')

numero2_cadena = '20'
numero2 = int(numero2_cadena)
print(f'Numero 2: {numero2}')

resultado = numero1 + numero2
print(f'Resultado: {resultado}')


