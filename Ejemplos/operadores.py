#operadores aritmeticos
a = 10
b = 3
#Suma
suma = a + b
print(suma)
#Resta
resta = a - b
print(resta)
#Multiplicacion
multiplicacion = a * b
print(multiplicacion)
#Division
division = a / b
print(f'{division:.2f}')
#Division Entera
div_entera = a // b
print(div_entera)
#Exponente-modulo
exponente = a ** b
print(exponente)